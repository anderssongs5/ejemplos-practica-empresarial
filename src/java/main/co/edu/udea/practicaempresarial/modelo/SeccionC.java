package co.edu.udea.practicaempresarial.modelo;

public class SeccionC {

    private String cotizacionObligatoria;
    private String totalDescuentos;
    private String numeroDiasMora;
    private String totalNetoAportes;

    public String getCotizacionObligatoria() {
        return cotizacionObligatoria;
    }

    public void setCotizacionObligatoria(String cotizacionObligatoria) {
        this.cotizacionObligatoria = cotizacionObligatoria;
    }

    public String getTotalDescuentos() {
        return totalDescuentos;
    }

    public void setTotalDescuentos(String totalDescuentos) {
        this.totalDescuentos = totalDescuentos;
    }

    public String getNumeroDiasMora() {
        return numeroDiasMora;
    }

    public void setNumeroDiasMora(String numeroDiasMora) {
        this.numeroDiasMora = numeroDiasMora;
    }

    public String getTotalNetoAportes() {
        return totalNetoAportes;
    }

    public void setTotalNetoAportes(String totalNetoAportes) {
        this.totalNetoAportes = totalNetoAportes;
    }
}
